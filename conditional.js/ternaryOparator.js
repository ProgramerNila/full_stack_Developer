// Ternary oparator
// if else er short version
// ternory oparator e first condition likte hobe,? dite hobe. then if block er code : else block er code;

let number = 10;
let isRight= (number > 50) ? true : false;
console.log(isRight);

// even or odd check
let isEven = (number % 2 === 0) ? 'even' : 'odd'
console.log(isEven);
let isOdd = (number % 2 !== 0) ? 'even' : 'odd'
console.log(isOdd);















